 #Um carro percorreu 150 km a uma velocidade média de 60 km/h. Quanto tempo (em horas) o carro levou para percorrer essa distância?
# fórmula: t = d/v -> d: distância                                                                             v: velocidade

tempo = 150 / 60
print("t: tempo\nd: distância (Km)\nv: velocidade (Km/h)\n")
print("t = d/v")
print(f"{tempo} horas")
